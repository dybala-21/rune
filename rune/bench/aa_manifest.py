def build_aa_attempt_matrix(manifest: dict[str, Any] | None = None) -> list[dict[str, Any]]:
    """Return the pinned 3-repeat attempt matrix used by the AA index."""
    manifest = manifest or build_artificial_analysis_manifest()
    validate_manifest(manifest)

    attempts: list[dict[str, Any]] = []
    for component in manifest["components"]:
        component_name = str(component["name"])
        benchmark = _BENCHMARK_SLUGS[component_name]
        repeats = int(component["repeats"])
        for task_id in component["task_ids"]:
            for attempt_index in range(1, repeats + 1):
                attempts.append(
                    {
                        "index": len(attempts) + 1,
                        "benchmark": benchmark,
                        "component": component_name,
                        "task_id": task_id,
                        "attempt_index": attempt_index,
                        "response_type": component["response_type"],
                        "scoring": component["scoring"],
                    }
                )
    return attempts


def validate_manifest(manifest: dict[str, Any]) -> None:
    """Raise ValueError when the pinned manifest has drifted internally."""
    components = manifest.get("components")
    if not isinstance(components, list):
        raise ValueError("manifest.components must be a list")

    expected_counts = {
        "SWE-Bench-Pro-Hard-AA": 150,
        "Terminal-Bench v2": 84,
        "SWE-Atlas-QnA": 124,
    }
    for component in components:
        name = component.get("name")
        if name not in expected_counts:
            raise ValueError(f"unknown component: {name}")
        task_ids = component.get("task_ids")
        if not isinstance(task_ids, list):
            raise ValueError(f"{name}.task_ids must be a list")
        if len(task_ids) != expected_counts[name]:
            raise ValueError(f"{name} expected {expected_counts[name]} tasks, got {len(task_ids)}")
        if len(set(task_ids)) != len(task_ids):
            raise ValueError(f"{name} contains duplicate task IDs")

    if manifest.get("task_count") != 358:
        raise ValueError(f"manifest.task_count expected 358, got {manifest.get('task_count')}")
    if manifest.get("attempt_count") != 1074:
        raise ValueError(f"manifest.attempt_count expected 1074, got {manifest.get('attempt_count')}")
