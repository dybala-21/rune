def _component(
    *,
    name: str,
    field: str,
    response_type: str,
    scoring: str,
    task_ids: tuple[str, ...],
    repeats: int = 3,
    extra: dict[str, Any] | None = None,
) -> dict[str, Any]:
    data: dict[str, Any] = {
        "name": name,
        "field": field,
        "questions": len(task_ids),
        "repeats": repeats,
        "response_type": response_type,
        "scoring": scoring,
        "task_ids": list(task_ids),
    }
    if extra:
        data.update(extra)
    return data


